    # control byte for data or commands
    COMMAND_BYTE = bytearray([0x00])  # commands
    DATA_BYTE    = bytearray([0x40])  # data

    # sequence to initiate a SSD1306 OLED display 
    # 
    #
    #     Only valid for 128 X 32 display
    #

    INIT_SEQ = COMMAND_BYTE + bytearray([
            0xA8, 0x1F,       # set multiplex ratio - 32 vertical pixels
            0xD3, 0x00,       # set display offset
            0x40,             # set start line
            0xA1,             # segment remap, SEG0 on col 0
            0xC0,             # regular COM scan direction
            0xDA, 0x02,       # COM pins config - 32 vertical pixels
            0x81, 0x7F,       # set contrast, no change after A7
            0xA4,             # display from RAM
            0xA6,             # A6 = normal display, A7 = inverse
            0xD5, 0x80,       # set clock divide
            0x8D, 0x14,       # charge pump
            0x20, 0x00,       # set horizontal memory addressing mode
            0x21, 0x00, 0x7F, # col address from 0 to 127
            0x22, 0x00, 0x03, # page address from 0 to 3
            0xAF,             # display ON
            0x2E,             # deactivate scroll
        ])    

